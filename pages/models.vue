<p>(Interfacial Surface Generator) Mixer is a continuous process, in-line mixer having no moving parts. It offers mathematically predictable layer generation in a wide variety of liquid-liquid, liquid-solid, and gas-liquid mixing applications. The mixer can quickly be installed in new and existing pipelines at low initial and operating costs. Since it has no moving parts, there is no wear and as a result, no maintenance is required.</p>

<h3>Mixing Principle</h3>
<p>The ISG Mixer consists of mixing elements enclosed in a pipe housing. Elements can be easily removed from the housing for individual cleaning. The ends of the elements are shaped so that adjacent elements form a tetrahedral chamber. Four holes bored through each element provide the flow paths. If two input streams enter an ISG mixer, the number of layers emerging from the first, second and third elements are 8, 32, 128. This exponential progression generates over two million layers in just 10 elements.</p>

<p align="center"><img src="https://cms.mixers.com/assets/EBDB6A6C-2BD2-4A92-A958-4D8E35AD25B3?width=1200&quality=50&format=webp&transforms=[[%22sharpen%22]]" width="400"></p>

<p>The four holes in each ISG element are at oblique angles so that material near the periphery (i.e. near the pipe wall) on the inlet side of the element emerges near the center on the outlet side. This radial mixing eliminates the wall smearing effect often encountered otherwise.</p>

<h3>Construction</h3>
<p>ISG mixers are available in from 5/8" to 6" diameter. Units are constructed of stainless steel, Teflon or polypropylene. Other materials and larger sizes can be fabricated to specifications. Threaded, tri-clamp or flanged connections are available on all sizes.</p>

<h3>Application</h3>
<p>The Ross ISG mixer is ideal for applications where mixing elements must be removed for fast, easy cleaning - such as for sanitary requirements. The mixer is unsurpassed in blending materials with large viscosity ratios, as high as 250,000:1 and more.</p>


