            <h2>LPD Static Mixers</h2>
            <img src="https://cms.mixers.com/assets/B1CEBC7A-BC18-4FAF-84BE-6D0224CD8661?width=1200&quality=50&format=webp&transforms=[[%22sharpen%22]]" class="fright" width="400">
<p>(Low Pressure Drop) Static Mixer is an exceptionally efficient in-line mixer that contains no moving parts. It is easily installed in new and existing pipelines and offers both low installation and low operating costs. With no moving parts to wear out, it is completely trouble free, requires no maintenance and has an infinite life. Its patented design provides mathematically predictable mixing results of liquids or gases with very low pressure drop. There are virtually no viscosity limitations.</p>

<h3>Mixing Principle</h3>
<p>The LPD Mixer consists of a series of semi-elliptical plates which are discriminately positioned in a tubular housing. A single element consists of two plates perpendicular to each other. The mixing operation is based on splitting and then diverting input streams. In laminar flow, the number of layers (L) formed by (E) elements with an initial input of (N) components is given by L = N(2)E. In turbulent flow, the elements enhance the random dispersion of sub-streams so that an LPD with six elements is sufficient for most low viscosity mixing requirements.</p>

<h3>Construction</h3>
<p>The mixing elements are not normally welded to the housing, but as an option they can be welded in place. LPD Mixers are available in standard pipe from 1" to 30" diameter in 2-element to 36- element modules. There is no limit to how large the mixer can be. The mixer is fabricated in stainless steel type 316 and carbon steel. Other construction materials are also available. The mixer can be ordered with plain, threaded or flanged ends or with special end connections. It can also be furnished with a heating or cooling jacket.</p>

<h3>Sizing</h3>
<p>The diameter and number of elements in an assembly depend on the degree of mixing required. As a general rule, the diameter of the mixer should be the same size as the process pipeline. Where available pressure drop is limited, select a larger diameter mixer. The number of elements required and the pressure drop for a given application may be determined from the Mixer Selector section.</p>

